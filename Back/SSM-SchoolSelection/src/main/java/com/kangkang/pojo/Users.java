package com.kangkang.pojo;

public class Users {
	private Integer id;
	private String depart_id;
	private String depart;
	private String username;
	private String password;
	private String depart_permission;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getDepart_id() {
		return depart_id;
	}
	public void setDepart_id(String depart_id) {
		this.depart_id = depart_id;
	}
	public String getDepart() {
		return depart;
	}
	public void setDepart(String depart) {
		this.depart = depart;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getDepart_permission() {
		return depart_permission;
	}
	public void setDepart_permission(String depart_permission) {
		this.depart_permission = depart_permission;
	}
	
}
